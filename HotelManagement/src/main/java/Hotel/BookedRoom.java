package Hotel;

public class BookedRoom {
public int roomNo;
public String roomType;
public int totalCost;
public int days;



public BookedRoom(int roomNo, String roomType, int totalCost, int days) {
	this.roomNo = roomNo;
	this.roomType = roomType;
	this.totalCost = totalCost;
	this.days = days;
}
}
