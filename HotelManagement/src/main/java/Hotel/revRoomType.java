package Hotel;

public class revRoomType {
	String roomType;
	int Amount;
	public revRoomType(String roomType, int amount) {
		super();
		this.roomType = roomType;
		Amount = amount;
	}
	public String getRoomType() {
		return roomType;
	}
	public int getAmount() {
		return Amount;
	}
	

}
