package Hotel;

public class IncomeObj {
	int id;
	String name;
	int cost;
	public IncomeObj(int id, String name, int cost) {
		this.id = id;
		this.name = name;
		this.cost = cost;
	}
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public int getCost() {
		return cost;
	}
	

}
